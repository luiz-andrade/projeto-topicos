            <h3>O que é um TCC?</h3>

            &nbsp; &nbsp; &nbsp;TCC significa Trabalho de Conclusão de Curso. Criado em 1983, como disciplina obrigatória no curso de Pedagogia da Universidade de Franca, logo tornou-se institucional e se estendeu a todos os cursos de graduação. Refere-se a uma dissertação científica, do cunho monográfico iniciático, que os alunos concluintes devem elaborar. Procurou-se, por meio desta exigência, criar espaço para os estudantes iniciarem-se no campo da pesquisa, buscando ampliar os conhecimentos teóricos acumulados ao longo da graduação.

